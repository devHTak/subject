DROP TABLE IF EXISTS "MEMBER";
CREATE TABLE "MEMBER" (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY (start with 1) PRIMARY KEY,
    "NAME" VARCHAR(255) NOT NULL,
    "OWNED_AMOUNT" BIGINT,
    "JOIN_YN" VARCHAR(1),
    "CREATED_AT" TIMESTAMP NOT NULL,
    "MODIFIED_AT" TIMESTAMP NOT NULL
);

DROP TABLE IF EXISTS "MEMBER_PAYMENT_LIMIT";
CREATE TABLE "MEMBER_PAYMENT_LIMIT" (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY (start with 1) PRIMARY KEY,
    "MEMBER_ID" BIGINT NOT NULL,
    "PAYMENT_LIMIT_TYPE" VARCHAR(255) NOT NULL,
    "LIMIT_AMOUNT" BIGINT,
    "CREATED_AT" TIMESTAMP NOT NULL,
    "MODIFIED_AT" TIMESTAMP NOT NULL
);

DROP TABLE IF EXISTS "PAYMENT";
CREATE TABLE "PAYMENT" (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY (start with 1) PRIMARY KEY,
    "MEMBER_ID" BIGINT NOT NULL,
    "PAYMENT_TYPE" VARCHAR(255) NOT NULL,
    "PAYMENT_AMOUNT" BIGINT,
    "PAYMENT_AT" TIMESTAMP NOT NULL,
    "CREATED_AT" TIMESTAMP NOT NULL,
    "MODIFIED_AT" TIMESTAMP NOT NULL
);

DROP TABLE IF EXISTS "PAY_BACK";
CREATE TABLE "PAY_BACK" (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY (start with 1) PRIMARY KEY,
    "PAYMENT_ID" BIGINT NOT NULL,
    "PAY_BACK_TYPE" VARCHAR(255) NOT NULL,
    "PAY_BACK_AMOUNT" BIGINT,
    "PAY_BACK_AT" TIMESTAMP NOT NULL,
    "CREATED_AT" TIMESTAMP NOT NULL,
    "MODIFIED_AT" TIMESTAMP NOT NULL
);

